from pathlib import Path

import networkx as nx
from loguru import logger


class GraphReportGenerator:
    """Generates a markdown report summarizing codebase structural insights."""

    def __init__(self, G: nx.Graph, root_path: Path):
        self.G = G
        self.root_path = root_path

    def generate(self) -> str:
        """Produces a comprehensive structural report."""
        if self.G.number_of_nodes() == 0:
            return "# Codebase Structural Report\n\nNo graph data available."

        report = [
            "# Antigravity Codebase Structural Report",
            "\n> Analysis of the system's neural map to identify god nodes, architectural bridges, and hotspots.",
            "\n## Vital Statistics",
            f"- **Total Nodes**: {self.G.number_of_nodes()}",
            f"- **Total Edges**: {self.G.number_of_edges()}",
            f"- **Average Degree**: {sum(dict(self.G.degree()).values()) / self.G.number_of_nodes():.2f}",
        ]

        # 1. God Nodes (Highest Degree)
        report.append("\n## 🏛️ God Nodes (High Centrality)")
        report.append(
            "These nodes represent central components that many others depend on."
        )

        degrees = sorted(self.G.degree(), key=lambda x: x[1], reverse=True)
        for nid, deg in degrees[:10]:
            label = self.G.nodes[nid].get("label", nid)
            group = self.G.nodes[nid].get("group", "unknown")
            report.append(f"- **{label}** ({group}): {deg} connections")

        # 2. Structural Bridges
        # (Edges between communities)
        report.append("\n## 🌉 Architectural Bridges")
        report.append("Components that link different functional communities.")

        bridges = []
        for u, v, _data in self.G.edges(data=True):
            u_comm = self.G.nodes[u].get("community", -1)
            v_comm = self.G.nodes[v].get("community", -2)
            if u_comm != v_comm and u_comm >= 0 and v_comm >= 0:
                bridges.append((u, v))

        for u, v in bridges[:10]:
            u_label = self.G.nodes[u].get("label", u)
            v_label = self.G.nodes[v].get("label", v)
            report.append(f"- {u_label} ↔ {v_label}")

        # 3. Hotspots (Rationale nodes)
        report.append("\n## 🔥 Technical Debt & Logic Hotspots")
        report.append("Areas with significant notes, TODOs, or complex logic markers.")

        rationales = [
            n for n, d in self.G.nodes(data=True) if d.get("group") == "rationale"
        ]
        if rationales:
            for rid in rationales[:10]:
                data = self.G.nodes[rid]
                label = data.get("label", "Unknown Note")
                path = data.get("path", "Unknown Path")
                line = data.get("line", "?")
                report.append(f"- **{path}:L{line}**: {label}")
        else:
            report.append("- No significant hotspots detected.")

        # 4. Strategic Recommendations
        report.append("\n## 💡 Strategic Recommendations")
        if len(degrees) > 0 and degrees[0][1] > 20:
            report.append(
                f"- Consider refactoring **{self.G.nodes[degrees[0][0]].get('label')}** as it is becoming a 'God Object'."
            )

        if len(bridges) > 15:
            report.append(
                "- The system has high cross-module coupling. Review bridge components for cleaner interface definitions."
            )

        report.append("\n---\n*Report generated by GraphifyEngine & Antigravity Nexus*")

        return "\n".join(report)

    def save(self, output_path: Path):
        """Saves the report to a file."""
        content = self.generate()
        output_path.write_text(content, encoding="utf-8")
        logger.info(f"GRAPH_REPORT: Saved to {output_path}")
        return output_path
